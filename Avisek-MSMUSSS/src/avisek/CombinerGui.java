    private boolean check_participants()
    {
        String hash;
        boolean b=true;
        for(int i=0;i<ref.Participant_No;i++)
        {
            hash=md5.md5Input(str[i], ref.bit_prime);
            //System.out.println(hash+" "+ref.hashes[i]);
            if(!(hash.equals(ref.hashes[i])))
            {
                b=false;
                ref.check[i]=1;
            }
        }
        return b;
    }

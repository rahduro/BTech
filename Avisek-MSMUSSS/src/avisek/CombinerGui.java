    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int chk=0;
        
        for(int i=0;i<ref.Participant_No;i++)
        {
            if(str[i].equals(""))
                chk=1;
        }

        if(chk==0)
        {
            if(!check_participants())
            {
                this.dispose();
                new BadParticipant().setVisible(true);
            }
            else
            {
                for(int i=0;i<ref.Participant_No;i++)
                    ref.U[i] = new BigInteger(str[i],16);
                this.dispose();
                ref.decrypt();
                new SecretDisplayGui().setVisible(true);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Please fill in all the Fields for all Participants before Pressing Proceed","Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

